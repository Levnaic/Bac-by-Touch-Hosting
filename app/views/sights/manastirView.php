    <p>
      Unutrašnjost crkve celokupno je prekrivena zidnim slikama (oko 600 m2), koje je 1737. godine izveo slikar i grafičar, Hristofor (Dž)Žefarović. Kao jedinstveno slikarsko delo svoga vremena, slikarstvo sjedinjuju stroge kanone poznovizantijske umetnosti sa barokom, kao modernim evropskim shvatanjima toga doba – predstavljajući tako prelomnu tačku u srpskoj umetnosti, i jedno od najvrednijih kompleksa zidnog slikarstva prve polovine 18. veka u jugoistočnoj Evropi. Ljiljana Stošić, istoričarka umetnosti o tome iznosi stav „Daleko nadišavši svoje vreme, Hristofor Žefarović je na bođanskim zidovima odlučno zacrtao buduće smernice ne samo srpskih umetničkih, nego i kulturnih puteva, odvažnim pokretima svoje slikarske četkice ih gotovo pre tri veka neraskidivo povezujući sa Evropom. Nije uzalud rečeno da u Bođanima, mada sve na svom mestu – ništa više nije bilo kao nekada, a na kompozicijama za koje prvoslavna umetnost dotad nije znala – kao da je sve bilo nekako prepoznatljivo.“ Ikonostas je, takođe izuzetna umetnička celina, nastala u više navrata − tokom 18. i početkom 19. veka, na kojoj se ističu dela kijevskih slikara Jova Vasilijeviča i Vasilija Romanoviča, kao i srpskih majstora Vasilija Ostojića i monaha Simeona Baltića. Bođanski ikonostas predstavlja jedan od prvih prodora baroknog slikarstva na području koje je do kraja 17. veka pripadalo Otomanskom carstvu. U ovom slučaju barokni uticaji dolaze posredno, preko ukrajinskih umetnika, koji su prihvatili zapadnoevropski manir i prilagodili ga pravoslavnom slikarskom nasleđu.<br><br>
      U severnom delu naosa nalazi se Bogorodičin tron sa čudotvornom ikonom Bogorodice Bođanske sa kraja 17. veka. Ova ikona je smatarna zaštitnicom manastira i Bačke eparhije. Značaj manastirske biblioteke i arhiva i danas je vrlo veliki zbog unikatnosti materijala, kao i zapisa na njima iz raznih perioda.
      <br><br>
      Uvećanje znanja o manastiru Bođani u funkciji konzervacije odvijalo se u pravcu boljeg poznavanja izuzetno vrednog zidnog slikarstva manastirske crkve, tehnika i slikarske palete Hristofora (Dž)Žefarovića i razumevanja složenih fenomena njegove degradacije. Na ovom zadatku okupili su se stručnjaci i naučnici iz Srbije, ali i van njenih granica, koji su pored projekta Vekova Bača delovali i u okviru dva italijansko-srpska projekta. Sveobuhvatnim istraživanjima došlo se do preporuka za intervencije, dokazujući značaj eksperatskog angažovanja, umrežavanja, timskog rada i međunarodne saradnje. Multidisciplinarnost i interdisciplinarnost, kao jedan od osnovnih polazišta savremene konzervacije, iznova su u Bođanima potvrđivani na delu. Zajedničkim radom došlo se do unapređenja metodologije rada, izrade i obrade dokumentacije, kao polazne osnove za buduće konzervatore i istraživače. Odvijao se i proces podizanja profesionalne kvalifikacije za timski rad, koji će kasnije dobiti svoj organizovani vid kroz osnivanje i rad Laboratorije za ispitivanje materijala u kulturnom nasleđu pri Tehnološkom fakultetu u Novom Sadu.

