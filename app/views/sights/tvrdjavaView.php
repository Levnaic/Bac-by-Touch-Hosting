      Prvi tragovi o Baču potiču još iz perioda cara Justinijana I, kada car pominje Bač u svom pismu 535. godine. Bač je 873. godine bilo Avarsko utvrđenje. Za vreme vladavine Arpadovića, Bač je bio kraljevski grad i sedište biskupije. Godine 1241. u Bač su upali Mongoli i razorili ga. Tvrđavu u Baču, koja je danas najstarija tvrđava u Vojvodini, podigao je ugarski kralj Robert Anžujski u periodu između 1338. - 1342. godine. Tvrđava je podignuta na manjem ostrvu nastalom od meandra reke Mostonge. Spada u takozvane "Vodene Gradove", jer je sa svih strana bila opasana rekom, a prilazilo joj se pokretnim mostovima. U periodu nakon Mohačke bitke 1529. godine grad i tvrđava bivaju osvojeni od strane Turaka. Iz perioda turske vladavine potiču i opisi Bačke Tvrđave u putopisima Evlije Čelebije, kada Bač postaje deo segedinskog sandžaka. Od vremena Rakocijeve bune (1702 - 1704) tvrđava biva spaljena, razrušena i napuštena, nakon čega više nije obnavljana. Tvrđava u Baču je najbolje očuvani srednjevekovni fortifikacioni kompleks u Vojvodini, koji pripada tipu "vodenog grada", osmišljenog da brani grad u močvarnim, ravnim terenima. Tvrđava je veoma važan višeslojni lokalitet i istorijsko mesto. Osnovu tvrđave čini nepravilni petougaonik sa četiri isturene kule na uglovima, koje su međusobno bile povezane obimnim zidom, danas sačuvanim u manjem procentu. Kule su različitih oblika i dimenzija. Tri ugaone kule imaju kružnu osnovu, dok je severozapadna četvorougaone osnove. Jedini slobodnostojeći i obnovljeni objekat unutar tvrđave je donžon kula, kvadratne osnove, smeštena u istočnom delu tvrđave. Sa južne strane nalaze se ostaci izdvojene odbrambene kule - barbakana. Tokom arheoloških iskopavanja 2004-2006. istražen je objekat prislonjen uz severoistočni bedem, definisan kao palata, kao i cesterna kružne osnove na prostoru između donžon kule i jugoistočnog bedema. Kompleks je nedovoljno istražen, isturene kule su u gornjim zonama nedostupne i nisu bezbedne za neposredna istraživanja, usled čega nedostaju potrebni podaci o konstrukcijama i tehnikama građenja. Na žalost, mnogi podaci su trajno izgubljeni i izbrisani. Imajući u vidu okolnost da su započeta sistematska arheološka istraživanja i ispitivanje materijala, sada se stvaraju uslovi za izoštravanje fokusa na različita polja otvorenih pitanja vezanih za građenje.
